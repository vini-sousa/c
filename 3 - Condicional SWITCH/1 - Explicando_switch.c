/* A estrutura condicional switch possui uma lógica de funcionamento muito diferente de todas as variantes das estruturas if que estudamos até agora. Ela não avalia diversas condições para determinar qual opção executar, tal como era o caso das estruturas if. 

O switch é uma construção de múltiplas possibilidades de decisão. Ele compara o resultado de uma expressão com uma série de valores constantes.

Primeiro, o comando switch avalia a expressão. 
Em seguida, começa a comparar o resultado da expressão com o valor de cada uma das possibilidades marcadas pela palavra reservada case, na sequência dada. 
Cada possibilidade deve ser um número inteiro (tipo int, long, short e variantes) ou um caractere (tipo char e variantes). */ 

